package com.event.InnerEntities;

public class LatLongPiar {
	private Double pointx;// 事件发生的经度
	private Double pointy;// 事件发生的纬度
	private Double gaodu;// 事件发生点的高度

	public Double getGaodu() {
		return gaodu;
	}

	public void setGaodu(Double gaodu) {
		this.gaodu = gaodu;
	}

	public Double getPointx() {
		return pointx;
	}

	public void setPointx(Double pointx) {
		this.pointx = pointx;
	}

	public Double getPointy() {
		return pointy;
	}

	public void setPointy(Double pointy) {
		this.pointy = pointy;
	}

}
