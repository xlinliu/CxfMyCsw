package com.event.InnerEntities;

public class PrecationInfo {
	private String satelliteData;
	private String UAVData;
	private String groundData;

	public String getSatelliteData() {
		return satelliteData;
	}

	public void setSatelliteData(String satelliteData) {
		this.satelliteData = satelliteData;
	}

	public String getUAVData() {
		return UAVData;
	}

	public void setUAVData(String uAVData) {
		UAVData = uAVData;
	}

	public String getGroundData() {
		return groundData;
	}

	public void setGroundData(String groundData) {
		this.groundData = groundData;
	}

}
