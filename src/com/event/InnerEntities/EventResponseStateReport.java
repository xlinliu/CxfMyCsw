package com.event.InnerEntities;

public class EventResponseStateReport {
	private String repairedRoadsUrl;
	private String searchedRegionsURL;
	private String repairedFortificationURL;

	public String getRepairedRoadsUrl() {
		return repairedRoadsUrl;
	}

	public void setRepairedRoadsUrl(String repairedRoadsUrl) {
		this.repairedRoadsUrl = repairedRoadsUrl;
	}

	public String getSearchedRegionsURL() {
		return searchedRegionsURL;
	}

	public void setSearchedRegionsURL(String searchedRegionsURL) {
		this.searchedRegionsURL = searchedRegionsURL;
	}

	public String getRepairedFortificationURL() {
		return repairedFortificationURL;
	}

	public void setRepairedFortificationURL(String repairedFortificationURL) {
		this.repairedFortificationURL = repairedFortificationURL;
	}
}
