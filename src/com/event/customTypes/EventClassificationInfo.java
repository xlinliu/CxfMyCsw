package com.event.customTypes;

public class EventClassificationInfo {
	private String eventcategory;// 时间分类
	private String eventPattern;// 事件模式
	private String eventInheritance;// 事件层次
	private String eventUrgency;// 事件紧急程度
	private String eventSeverity;// 事件严重性
	private String eventCertainty;// 事件确定性

	public String getEventcategory() {
		return eventcategory;
	}

	public void setEventcategory(String eventcategory) {
		this.eventcategory = eventcategory;
	}

	public String getEventPattern() {
		return eventPattern;
	}

	public void setEventPattern(String eventPattern) {
		this.eventPattern = eventPattern;
	}

	public String getEventInheritance() {
		return eventInheritance;
	}

	public void setEventInheritance(String eventInheritance) {
		this.eventInheritance = eventInheritance;
	}

	public String getEventUrgency() {
		return eventUrgency;
	}

	public void setEventUrgency(String eventUrgency) {
		this.eventUrgency = eventUrgency;
	}

	public String getEventSeverity() {
		return eventSeverity;
	}

	public void setEventSeverity(String eventSeverity) {
		this.eventSeverity = eventSeverity;
	}

	public String getEventCertainty() {
		return eventCertainty;
	}

	public void setEventCertainty(String eventCertainty) {
		this.eventCertainty = eventCertainty;
	}
}
